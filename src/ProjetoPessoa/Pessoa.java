package ProjetoPessoa;

public class Pessoa {
    String nome;
    String cpf;

    Pessoa( String nome, String cpf){
         this.nome = nome;
         this.cpf = cpf;

    }

    public void setnome(String nome){
        this.nome = nome;
    }

    public String getNome(){
        return this.nome = nome;
    }

    public void setCpf(String cpf) {
        this.cpf = cpf;
    }

    public String getCpf(){
        return this.cpf = cpf;
    }

}
